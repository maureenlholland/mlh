function toggleAnimationState() {
    // change particles json
    var obj = pJSDom[0].pJS;
    var isMoving = !obj.particles.move.enable;
    obj.particles.move.enable = isMoving;
    // update animation button
    animationState.setAttribute('data-animation-state', isMoving)
    animationState.textContent = isMoving ? 'Stop animation' : 'Start animation';
    // refresh particles with new move state
    obj.fn.particlesRefresh();
}

animationState.addEventListener('click', toggleAnimationState); 